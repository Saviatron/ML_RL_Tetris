package com.uhu.utils;

import java.io.Serializable;

public class Respuesta implements Serializable {

	private static final long serialVersionUID = -8498507436368771844L;
	private int DESPLAZAR;
	private int ROTAR;

	public Respuesta(int _desp, int _rot) {
		DESPLAZAR = _desp;
		ROTAR = _rot;
	}

	@Override
	public String toString() {
		return "mov;" + DESPLAZAR + ";" + ROTAR + ";";
	}

	public int getDESPLAZAR() {
		return DESPLAZAR;
	}

	public int getROTAR() {
		return ROTAR;
	}

}
